package com.samara;

public class Dimensions {
	// properties
	private int width;
	private int height;
	private int depth;

	// constructors
	public Dimensions(int width, int height, int depth) {
		this.width = width;
		this.height = height;
		this.depth = depth;
	}

	// getters
	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}

	public int getDepth() {
		return depth;
	}
}
