package com.samara;

public class Resolution {
	// properties
	private int width;
	private int height;

	// constructors
	public Resolution(int width, int height) {
		this.width = width;
		this.height = height;
	}

	// getters
	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
}
