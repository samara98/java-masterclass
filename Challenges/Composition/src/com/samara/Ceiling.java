package com.samara;

public class Ceiling {
	// properties
	private int height;
	private int paintedColor;

	// constructors
	public Ceiling(int height, int paintedColor) {
		this.height = height;
		this.paintedColor = paintedColor;
	}

	// getters
	public int getHeight() {
		return height;
	}

	public int getPaintedColor() {
		return paintedColor;
	}
}
